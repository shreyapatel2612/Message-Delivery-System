    /**
    * Provides method to display message on message box
    */
    public synchronized void displayMessageOnUI(String message) {
        if (!message.equals("")) {
            messageBox.append("\n-----------------------------------------------------------------------------------------\n");
            messageBox.append(message);
            System.out.println("message = " + message);
            messageBox.append("\n-----------------------------------------------------------------------------------------\n");
        }
    }
