    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
       
        /* Input validation */
        if (studentTextField.getText().isEmpty() || courseTextField.getText().isEmpty()) {
            displayMessageOnUI("Please provide course name and student name both");
            return;
        }
        try {
            /* Send push request to MQS */
            studentHandler.sendMqsRequest(studentTextField.getText(), courseTextField.getText());
            displayMessageOnUI("Send request to MQS. Student: " + studentTextField.getText() + " Course Name: " + courseTextField.getText());
        } catch (Exception ex) {
            System.out.println("Unable to perform send action in StudentGUIHandler");
        }
    }//GEN-LAST:event_sendButtonActionPerformed

    
